const supportedCountrySet = new Set<string>(SUPPORTED_COUNTRIES);
const studyDevices = ['phone', 'tablet', 'laptop', 'desktop'] as const;

const trimmedString = () => z.string().trim();
const emailSchema = z.string().trim().toLowerCase().email();
const countrySchema = z
  .string()
  .trim()
  .toUpperCase()
  .refine((country) => supportedCountrySet.has(country), 'Unsupported country code.');
const timeZoneSchema = z
  .string()
  .trim()
  .refine((timeZone) => {
    try {
      new Intl.DateTimeFormat('en-US', { timeZone }).format();
      return true;
    } catch {
      return false;
    }
  }, 'Invalid IANA time zone identifier.');

export const registerSchema = z.object({
  email: emailSchema,
  password: z.string().min(8).max(128),
  displayName: trimmedString().min(2).max(100),
  country: countrySchema,
  timeZone: timeZoneSchema,
  primaryStudyDevice: z.enum(studyDevices).nullable().optional(),
  acceptedTerms: z.literal(true),
  acceptedPrivacyPolicy: z.literal(true),
});
export type RegisterInput = z.infer<typeof registerSchema>;
export const verifyEmailSchema = z.object({ email: emailSchema, token: z.string().min(10) });
export const firebaseResyncSchema = z.object({ firebaseIdToken: z.string().min(10) });
export const loginSchema = z.object({
  email: emailSchema,
  password: z.string().optional(),
  mfaCode: z.string().optional(),
  firebaseIdToken: z.string().min(10),
});
export const refreshSchema = z.object({ refreshToken: z.string().min(32) });
export const forgotPasswordSchema = z.object({ email: emailSchema });
export const resetPasswordSchema = z.object({ email: emailSchema.optional() }).passthrough();
export const revokeSchema = z.object({
  refreshToken: z.string().min(32),
  reason: z.string().max(200).default('user_revoke'),
});
